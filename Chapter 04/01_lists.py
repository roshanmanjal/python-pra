# list
items = ["Roshan" , 36, "Rohan", 45, "Tea" ]
print(items[4])
# now we can change any item in this
items[1] = 32
print(items[1])

# also we can append (add)
items.append("Kiris")
print(items)

# also we can reverses list
l1 = [1,2,3,4,5,6]
print(l1)
l1.reverse()
l2 = [1,45,45,67,3,45,65,86,78,8,75]
l2.sort() # it will sort
print(l1)
print(l2)
l1.pop(3) # 3rd index items will removed
print(l1)